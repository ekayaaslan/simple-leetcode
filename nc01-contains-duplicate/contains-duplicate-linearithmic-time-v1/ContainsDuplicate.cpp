// https://leetcode.com/problems/contains-duplicate
// Linearithmic time, constant extra-space
// Concepts: Sorting, Linear search.
class ContainsDuplicate {
public:
    bool containsDuplicate(vector<int>& nums) {
        sort(nums.begin(), nums.end());
        int n = nums.size();
        for (int i=1; i<n; i++) {
            if (nums[i] == nums[i-1]) {
                return true;
            }
        }
        return false;
    }
};
